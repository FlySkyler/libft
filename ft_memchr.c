#include "libft.h"

void	*ft_memchr(const void *s, int c, t_size n)
{
	t_size			i;
	unsigned char	*str;

	str = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (str[i] == c)
			return ((void *)&str[i]);
		i++;
	}
	return (0);
}
